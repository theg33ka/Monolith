using System.Linq;
using Content.Shared.Access;
using Content.Shared.Roles;
using Content.Shared.StatusIcon;
using Content.Shared._Forge.Access;
using Content.Shared._Forge.Access.Systems;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Log;
using Robust.Shared.Prototypes;

namespace Content.Client._Forge.Access.UI;

[GenerateTypedNameReferences]
public sealed partial class JobPresetConsoleWindow : DefaultWindow
{
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;
    [Dependency] private readonly IEntitySystemManager _entitySystemManager = default!;
    [Dependency] private readonly ILogManager _logManager = default!;

    private readonly JobPresetConsoleBoundUserInterface _owner;
    private readonly Dictionary<ProtoId<JobPrototype>, PresetButtonEntry> _presetButtons = new();
    private readonly SpriteSystem _spriteSystem;
    private readonly ISawmill _sawmill;

    private sealed record PresetButtonEntry(JobPresetListItem Button, JobPrototype Job);

    public JobPresetConsoleWindow(
        JobPresetConsoleBoundUserInterface owner,
        List<ProtoId<JobPrototype>> rankPresets)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _owner = owner;
        _spriteSystem = _entitySystemManager.GetEntitySystem<SpriteSystem>();
        _sawmill = _logManager.GetSawmill(SharedJobPresetIdCardConsoleSystem.Sawmill);

        PopulatePresets(rankPresets);
    }

    private void PopulatePresets(List<ProtoId<JobPrototype>> rankPresets)
    {
        RankPresetContainer.DisposeAllChildren();
        _presetButtons.Clear();

        var buttonGroup = new ButtonGroup();

        foreach (var presetId in rankPresets)
        {
            if (!_prototypeManager.TryIndex<JobPrototype>(presetId, out var job))
            {
                _sawmill.Error($"Unable to find job preset '{presetId}' for preset ID card console.");
                continue;
            }

            var button = new JobPresetListItem(new JobPresetListItem.ViewState(
                job.LocalizedName,
                job.LocalizedDescription ?? string.Empty,
                GetJobIcon(job)))
            {
                Group = buttonGroup,
            };
            button.SetTooltips(job.LocalizedName);
            button.ApplyPressed += () => _owner.ApplyPreset(job.ID);
            button.CreateInjectorPressed += () => _owner.CreateInjector(job.ID);

            RankPresetContainer.AddChild(button);
            _presetButtons[job.ID] = new PresetButtonEntry(button, job);
        }
    }

    public void UpdateState(JobPresetIdCardConsoleBoundUserInterfaceState state)
    {
        PrivilegedIdButton.Text = state.IsPrivilegedIdPresent
            ? Loc.GetString("id-card-console-window-eject-button")
            : Loc.GetString("id-card-console-window-insert-button");
        PrivilegedIdLabel.Text = state.PrivilegedIdName;

        TargetIdButton.Text = state.IsTargetIdPresent
            ? Loc.GetString("id-card-console-window-eject-button")
            : Loc.GetString("id-card-console-window-insert-button");
        TargetIdLabel.Text = state.TargetIdName;

        var interfaceEnabled =
            state.IsPrivilegedIdPresent &&
            state.IsPrivilegedIdAuthorized &&
            state.IsTargetIdPresent;

        CurrentRankValue.Text = GetCurrentRankText(state);
        PermissionHintLabel.Text = GetHintText(state);
        PermissionHintLabel.Modulate = interfaceEnabled ? Color.LightGray : Color.Red;

        UpdatePresetStates(
            state.TargetIdAccessList?.ToHashSet() ?? new HashSet<ProtoId<AccessLevelPrototype>>(),
            state.AllowedModifyAccessList?.ToHashSet() ?? new HashSet<ProtoId<AccessLevelPrototype>>(),
            state.TargetIdJobPrototype,
            interfaceEnabled);
    }

    private void UpdatePresetStates(
        HashSet<ProtoId<AccessLevelPrototype>> currentTags,
        HashSet<ProtoId<AccessLevelPrototype>> allowedTags,
        ProtoId<JobPrototype> currentJob,
        bool interfaceEnabled)
    {
        foreach (var (jobId, entry) in _presetButtons)
        {
            var blockReason = interfaceEnabled
                ? GetPresetBlockReason(jobId, currentTags, allowedTags)
                : null;
            var isCurrent = currentJob == jobId;
            var subtitle = isCurrent
                ? Loc.GetString("job-preset-id-card-console-window-current-rank")
                : blockReason ?? entry.Job.LocalizedDescription ?? string.Empty;
            var subtitleColor = blockReason == null ? Color.LightGray : Color.Red;

            entry.Button.Pressed = isCurrent;
            entry.Button.ApplyDisabled = !interfaceEnabled || blockReason != null;
            entry.Button.InjectorDisabled = !interfaceEnabled || blockReason != null;
            entry.Button.SetSubtitle(subtitle, subtitleColor);
            entry.Button.SetTooltips(blockReason ?? entry.Job.LocalizedDescription ?? entry.Job.LocalizedName);
        }
    }

    private Texture? GetJobIcon(JobPrototype job)
    {
        if (_prototypeManager.TryIndex(job.Icon, out JobIconPrototype? jobIcon))
            return _spriteSystem.Frame0(jobIcon.Icon);

        return null;
    }

    private string? GetPresetBlockReason(
        ProtoId<JobPrototype> jobId,
        HashSet<ProtoId<AccessLevelPrototype>> currentTags,
        HashSet<ProtoId<AccessLevelPrototype>> allowedTags)
    {
        if (!TryResolveJobAccess(jobId, out var presetTags))
            return Loc.GetString("job-preset-id-card-console-window-invalid-preset");

        var requiredAccess = presetTags
            .Union(currentTags);

        if (requiredAccess.All(allowedTags.Contains))
            return null;

        return Loc.GetString("job-preset-id-card-console-window-missing-access");
    }

    private bool TryResolveJobAccess(
        ProtoId<JobPrototype> jobId,
        out HashSet<ProtoId<AccessLevelPrototype>> tags)
    {
        tags = new HashSet<ProtoId<AccessLevelPrototype>>();

        if (!_prototypeManager.TryIndex(jobId, out JobPrototype? job))
            return false;

        tags.UnionWith(job.Access);

        foreach (var accessGroupId in job.AccessGroups)
        {
            if (!_prototypeManager.TryIndex(accessGroupId, out AccessGroupPrototype? group))
                continue;

            tags.UnionWith(group.Tags);
        }

        return true;
    }

    private string GetCurrentRankText(JobPresetIdCardConsoleBoundUserInterfaceState state)
    {
        if (!state.IsTargetIdPresent)
            return string.Empty;

        if (_prototypeManager.TryIndex(state.TargetIdJobPrototype, out JobPrototype? job))
            return job.LocalizedName;

        return Loc.GetString("job-preset-id-card-console-window-rank-unknown");
    }

    private string GetHintText(JobPresetIdCardConsoleBoundUserInterfaceState state)
    {
        if (!state.IsPrivilegedIdPresent)
            return Loc.GetString("job-preset-id-card-console-window-hint-insert-privileged");

        if (!state.IsTargetIdPresent)
            return Loc.GetString("job-preset-id-card-console-window-hint-insert-target");

        if (!state.IsPrivilegedIdAuthorized)
            return Loc.GetString("job-preset-id-card-console-window-hint-unauthorized");

        return Loc.GetString("job-preset-id-card-console-window-hint-authorized");
    }
}
